<script>
import * as d3 from 'd3'
import * as sinalService from '@/services/SinalService'
import * as fotoService from '@/services/FotoService'
import SinalCreate from '../components/SinalCreate.vue'
import SinalPreview from '../components/SinalPreview.vue'

export default {
  components: { SinalCreate, SinalPreview },
  props: ['interno', 'errors', 'readonly'],
  data: () => ({
    hoverId: null,
    srcImage: null,
    tipoImagem: null,
  }),
  mounted() {
    this.getAreas()
  },
  methods: {
    clearPreview() {
      this.hoverId = null
      this.srcImage = null
      this.tipoImagem = null
    },
    async getAreas() {      
      this.clearAreas()
      this.interno.sinais.map(item => {
        var element = document.getElementById(item.area)
        element.classList.add('st1')
        let area = item.area.toString()
        let bodyArea = null
        if (area.startsWith('frt')) {
          bodyArea = '#frt_body'
        }
        if (area.startsWith('bck')) {
          bodyArea = '#bck_body'
        }
        if (area.startsWith('lft')) {
          bodyArea = '#lft_body'
        }
        if (area.startsWith('rgt')) {
          bodyArea = '#rgt_body'
        }
        var body = d3.select(bodyArea)
        body.append('circle')        
          .attr('id', item.id)
          .attr('cx', item.position_x)
          .attr('cy', item.position_y)
          .attr('r', 15)
          .attr('stroke', '#FF8080')
          .attr('fill', '#FF0000')
          .on('click', this.onMouseOver)
      })
    },
    clearAreas(){      
      Array.from(document.querySelectorAll('.st1')).forEach((el) => el.classList.remove('st1'))
      d3.select('#frt_body').selectAll('circle').remove()
      d3.select('#bck_body').selectAll('circle').remove()
      d3.select('#lft_body').selectAll('circle').remove()
      d3.select('#rgt_body').selectAll('circle').remove()
    },
    onMouseOver(evt) {
      evt.preventDefault()
      if (this.hoverId != evt.srcElement.id) {
        this.srcImage = ''
        this.hoverId = evt.srcElement.id
        sinalService
          .getId(evt.srcElement.id)
          .then(res => {
            this.tipoImagem = res.data.tipo
            fotoService
              .getId(res.data.foto)
              .then(result => {
                this.srcImage = result.data.thumbnail
                this.$refs.preview.open(evt.clientX, evt.clientY, this.srcImage, this.tipoImagem, evt.srcElement.id, this.readonly) 
              })
          })
      }
    },
    onClickFront(evt) {
      if(evt.srcElement.id && !this.readonly){
        var svg = document.querySelector('#frt_body')
        var pt = svg.createSVGPoint()
        pt.x = evt.clientX; pt.y = evt.clientY
        var coords = pt.matrixTransform(svg.getScreenCTM().inverse())
        this.$refs.create.open(this.interno.id, evt.srcElement.id, coords.x, coords.y)
      }
    },
    onClickBack(evt) {
      if(evt.srcElement.id && !this.readonly){
        var svg = document.querySelector('#bck_body')
        var pt = svg.createSVGPoint()
        pt.x = evt.clientX; pt.y = evt.clientY
        var coords = pt.matrixTransform(svg.getScreenCTM().inverse())
        this.$refs.create.open(this.interno.id, evt.srcElement.id, coords.x, coords.y)
      }
    },
    onClickLeft(evt) {
      if(evt.srcElement.id && !this.readonly){
        var svg = document.querySelector('#lft_body')
        var pt = svg.createSVGPoint()
        pt.x = evt.clientX; pt.y = evt.clientY
        var coords = pt.matrixTransform(svg.getScreenCTM().inverse())
        this.$refs.create.open(this.interno.id, evt.srcElement.id, coords.x, coords.y)
      }
    },
    onClickRight(evt) {
      if(evt.srcElement.id && !this.readonly){
        var svg = document.querySelector('#rgt_body')
        var pt = svg.createSVGPoint()
        pt.x = evt.clientX; pt.y = evt.clientY
        var coords = pt.matrixTransform(svg.getScreenCTM().inverse())
        this.$refs.create.open(this.interno.id, evt.srcElement.id, coords.x, coords.y)
      }
    },
    createSinais(item) {
      this.srcImage = null
      this.hoverId = null
      this.tipoImagem = null
      this.interno.sinais.push(item)
      this.getAreas()
    },
    updateSinais(item) {
      const index = this.interno.sinais.findIndex(res => res.id === item.id)
      this.interno.sinais.splice(index, 1)
      this.srcImage = null
      this.hoverId = null
      this.tipoImagem = null
      this.interno.sinais.push(item)
      this.getAreas()
    },
    deletarSinais(item) {
      const index = this.interno.sinais.findIndex(res => res.id === item.id)
      this.interno.sinais.splice(index, 1)
      this.getAreas()
    }
  }
}
